# 2. Software development life cycle (SDLC)
# Waterfall model:
# Planning -> analysic -> design -> implementations -> maintenance
# a, Details:
# - Planning:
# Tìm ra 1 vấn đề và tìm cách xử lí nó,
# ví dụ: bạn có 1 vấn đề và cần làm 1 website để xử lí nó
# thì website đó là loại website gì? bao gồm nhưng chức năng gì ? Cần công cụ gì ?
# rất nhiều các câu hỏi cần phải giải quyết. What? how ? why
# - Analysic:
# Ở đây bạn sẽ phải trả lời các câu hỏi nên hay không nên, có hoặc không ?
# Tôi nên tạo một website với function a, b, c ? đúng bạn nên tạo
# cái bạn cần cái bạn không ? Tập trung vào những cái giá trị
# - Design:
# Chuẩn bị code, bao gồm các câu hỏi ? code web thì nên dùng gì ? 
# các bài toán a và b này thì có công nghệ gì để xử lí ?
# - Implementation:
# Bắt đầu code, fix bug ....
# - Maintenance:
# Monitoring
# Duy trì update (feature, công nghệ...)